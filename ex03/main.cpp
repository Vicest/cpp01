#include "HumanA.hpp"
#include "HumanB.hpp"

int main()
{
	{
		Weapon	club = Weapon("crude spiked club");
		HumanA	bob("Bob", club);

		bob.attack();
		club.setType("some other type of club");
		bob.attack();
	}
	{
		Weapon	club = Weapon("crude spiked club");
		HumanB	jim("Jim");

		jim.setWeapon(club);
		jim.attack();
		club.setType("some other type of club");
		jim.attack();
	}
	{
		Weapon	not_club = Weapon("zombie buster");
		HumanB	moloch("Moloch");

		moloch.attack();
		moloch.setWeapon(not_club);
		moloch.attack();
		not_club.setType("shotgun");
		moloch.attack();
	}
}
